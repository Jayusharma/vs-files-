//find whether the charactersitic value is lower,upper case or  numeric.
#include<iostream>
using namespace std;

int main()
{
  char a;
  cin>>a;
  if (a>=97&&a<=122)
  {
    cout<<"lower case letter "<<endl;
  }
  else if(a>=65&&a<=90)
  {
    cout<<"upper case letter "<<endl;
  }
  else if(a>=48&&a<=57)
{
    cout<<"numeric  "<<endl;
}

} 
  
  
  
  
  






  